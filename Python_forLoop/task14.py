#14. Calculate Total Transport Cost
"""
Create a list of daily transport expenses for a week and calculate the total amount spent.
"""
transport_expenses = [80, 60, 75, 90, 50, 85, 70]
total_transport_expense = 0
for x in transport_expenses :
    total_transport_expense += x
print("Total transport expense for a week is ksh :", total_transport_expense)

